way = input("Выбери путь (направо/налево/прямо):").strip().lower()
if way == "направо" or way == "правый":
    print("Направо поедешь — женату быть")
elif way == "налево" or way == "левый":
    print("Налево поедешь — богату быть")
elif way == "прямо" or way == "вперед":
    print("Прямо поедешь — убиту быть")
else:

    print("Такого пути нет!")
