#!/bin/bash

echo "=== Анализ дискового пространства ==="
echo "Файловая система и процент заполнения:"
echo "----------------------------------------"

df -h | awk 'NR>1 {print $1, $5}'

echo "----------------------------------------"

echo -e "\n=== Проверка на критические разделы (> 90%) ==="

critical_found=0

df -h | awk 'NR>1 {if ($5+0 > 90) {print $1, $5; exit 1}}' || critical_found=1

if [ $critical_found -eq 1 ]; then
    echo "Обнаружены критические разделы с заполнением > 90%."
else
    echo "Критических разделов не обнаружено."
fi

echo "----------------------------------------"
