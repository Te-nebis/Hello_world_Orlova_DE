#!/bin/bash

check_root() {
    if [ "$EUID" -ne 0 ]; then
        echo "Ошибка: данный скрипт необходимо запускать от имени суперпользователя (root)." >&2
        exit 1
    fi
}

# Вызов функции проверки
check_root

# Остальная часть вашего скрипта может находиться здесь
echo "Скрипт запущен от имени суперпользователя."
